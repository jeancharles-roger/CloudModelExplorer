package org.xid.explorer.result;

import java.util.HashMap;
import java.util.Map;

/**
 * ModelResultDescription contains information that describes a result handler.
 */
public class ModelResultDescription {

    private String type;

    private String path;

    private Map<String, String> parameters = new HashMap<>();

    public ModelResultDescription() {
    }

    public ModelResultDescription(String type) {
        this.type = type;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public Map<String, String> getParameters() {
        return parameters;
    }

    public void setParameters(Map<String, String> parameters) {
        this.parameters = parameters;
    }
}
